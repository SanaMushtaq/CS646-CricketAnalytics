def getCountryCode(country):
    countryCode = ''
    if country == "england":
        countryCode = '1'
    elif country == "australia":
        countryCode = '2'
    elif country == "southafrica":
        countryCode = '3'
    elif country == "westindies":
        countryCode = '4'
    elif country == "newzealand":
        countryCode = '5'
    elif country == "india":
        countryCode = '6'
    elif country == "pakistan":
        countryCode = '7'
    elif country == "sri lanka":
        countryCode = '8'
    return countryCode
